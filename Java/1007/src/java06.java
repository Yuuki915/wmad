// switch case

public class java06 {
    public static void main(String[] args) {

        String name = "John";

        switch(name) {
            case "eric":
                System.out.println("Eric");
                break;

            case "derrick":
                System.out.println("Derrick");
                break;

            case "john":
                System.out.println("John");
                break;

            default:
                System.out.println("Invalid Entry");
                break;
        }
    }
}
