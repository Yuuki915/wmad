// do while loop

public class doWhileLoop09 {
    public static void main(String[] args) {
        
        int i = 11;

        do{
            System.out.println(i);
            i++;
        } while(i <= 10);



        int x = 1, y = 1;

        do {
            x++;
            y++;
            System.out.println("x=" + x + " and y=" + y);
        } while(x < 10 || y < 10);


    }
}
